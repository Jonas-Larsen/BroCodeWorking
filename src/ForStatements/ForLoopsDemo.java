package ForStatements;

public class ForLoopsDemo {
    public static void main(String[] args) {

        // For-loops = executes a block of code a limited amount of times
        // In for loops there are a total of 3 statement,
        // 1) An index (I) that you can declare
        // 2) A condition
        // 3) An incramation or decremation by a certain amount.
        for (int i = 10; i >= 0;i-=2) {
            System.out.println(i);

        }
        System.out.println("Happy new year");

    }
}
