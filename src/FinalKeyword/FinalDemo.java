package FinalKeyword;

public class FinalDemo {
    public static void main(String[] args) {
        // final = anything that is declared final cannot be changed or updated later in your program
        //         Something people do when tehy declare something final, people usally make the variable name in uppercase

        final double pi = 3.13159;

        // pi = 4;

        System.out.println(pi);
    }
}
